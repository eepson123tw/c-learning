#include <iostream>

int main()
{
	char x (5);
	std::cout << "x is equal to:" << x << std::endl;
	std::cout << "Hi!!" << std::endl;
	std::cout << "My name is Alex." << std::endl; // std::endl is used to end the line
	std::cout << "Hi!!" << "123" << std::endl;
	int y{};
	std::cin >> y;
	std::cout << "y is equal to:" << y << '\n';
	return 0;
}

